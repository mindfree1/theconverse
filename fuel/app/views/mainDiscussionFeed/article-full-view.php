	<p>North Korea has said it may pull out of a summit with US President Donald Trump if the US insists it gives up its nuclear weapons unilaterally.</p> 
	<p>The highly anticipated meeting between Mr Trump and North Korea's Kim Jong-un is due to take place on 12 June.</p> 
	<p>But in an angry statement, North Korea's vice-foreign minister accused the US of making reckless statements and of harbouring sinister intentions.</p> 
	<p>He pointed the finger squarely at US National Security Adviser John Bolton. </p> 
	<p>'We do not hide our feeling of repugnance towards him,' Kim Kye-gwan said. </p> 
	<p>The White House has responded by saying it is still hopeful the meeting will go ahead. </p> 
	<p>'The president is ready if the meeting takes place. If it doesn't, we'll continue the maximum pressure campaign that's been ongoing,' said spokesperson Sarah Sanders. </p> 
	<p>The groundbreaking agreement for Mr Kim and Mr Trump to meet came about as North Korea said it was committed to denuclearising the Korean peninsula. </p> <p>Exactly what that would entail has remained unclear, but North Korea has invited foreign media to witness the dismantling of its main nuclear test site later this month.</p> 
