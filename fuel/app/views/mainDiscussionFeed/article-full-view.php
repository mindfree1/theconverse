		<p>The sudden change in tone from Pyongyang is said to have taken US officials by surprise. Analysts said North Korea could be trying to strengthen its hand before talks.</p> 
		<p>The US state department said it was continuing to plan the Trump-Kim meeting, and Mr Trump is yet to comment.</p> 
		<p>A Chinese government spokesman urged North Korea and the US to 'meet each other halfway' ahead of negotiations. </p> 
		<h2>Why the personal attack on John Bolton?</h2>
		<p>The hawkish conservative is a firm defender of US power and a confrontational advocate for wielding that strength abroad. He has previously said it would be 'perfectly legitimate' to carry out a pre-emptive strike on North Korea. 
		</p> 
		<p>In media interviews over the weekend, he said North Korea could follow a Libyan model of nuclear disarmament - Libya gave up its weapons' programmes in the early 2000s and only then secured economic aid and normalised relations with the US.</p> 
		<p>However during the 2011 uprising against Colonel Gaddafi's regime, Western powers intervened in Libya in favour of the rebels, and Gaddafi was captured and killed.</p> 
		<p>North Korea has in the past suggested that Libya may have escaped Western military intervention had it kept its nuclear weapons programme. </p> 
