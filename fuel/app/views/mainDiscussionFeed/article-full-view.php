		<p>Mr Bolton recently said North Korea could follow a 'Libya model' of verifiable denuclearisation, but this alarms Pyongyang, which watched Libya's Colonel Gaddafi give up his nuclear programme only for him to be killed by Western-backed rebels a few years later.</p> 
		<p>The BBC's Laura Bicker in Seoul says North Korea - which had long said its nuclear arsenal is essential for its survival as a state - is now making its demands clear.</p> 
		<h2>Why has the North changed its tune?</h2> 
		<strong>Laura Bicker, BBC News, Seoul </strong> 
		<p>The whole reason it has spent years building up a nuclear arsenal, at such a great cost, is for survival. </p> 
		<p>So to compare denuclearisation in North Korea with Libya or indeed Iraq as John Bolton did on Sunday is not going to offer much comfort. 
			<a href='http://www.bbc.co.uk/news/av/world-africa-15397569/how-colonel-muammar-gaddafi-s-power-collapsed-in-libya'>Both regimes collapsed</a>. 
		</p> 
		<p>This is also a warning shot to the Trump administration. They will be aware how much Mr Trump wants this summit and how it is being spun as a success brought about by his maximum pressure strategy. </p> 
		<p>There were signs this boasting irritated Pyongyang, but now it has decided to speak out through someone in a position of power. </p> 
		<p>North Korea wants the world to know that it is coming to the negotiating table from a position of strength, and they may feel that they are making all the concessions. </p> 
		<p>
			<a href='http://www.bbc.co.uk/news/world-asia-43846488'>They've suspended all missile tests</a>, 
			<a href='http://www.bbc.co.uk/news/world-us-canada-44056972'>released the three US detainees</a>, Kim Jong-un met President Moon 
			<a href='http://www.bbc.com/news/world-asia-43914208'>and the pair signed a declaration</a>, and they're about to dismantle a nuclear test site in front of international media. 
		</p> 
		<p>So to hear the Trump administration claiming credit for a deal they don't like has been a step too far. </p> 
		<p>These statements more than hint that North Korea is prepared to walk away from President Trump's summit in Singapore until it does hear a deal it does like.</p> 
		<h2>What else does the North's statement say?</h2> 
		<p>Mr Kim's statement, carried by state media, said that if the US 'corners us and unilaterally demands we give up nuclear weapons we will no longer have an interest in talks' and 'will have to reconsider' attending the 12 June summit in Singapore.</p> 
		<p>He said North Korea did have 'high hopes' but that it was 'very unfortunate that the US is provoking us ahead of the summit by spitting out ludicrous statements'.</p> 
